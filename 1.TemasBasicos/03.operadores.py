# Operadores de comparacion
print(3 > 4)

print(3 < 4)

print(5 >= 4)

print(2 <= 4)

print(3 == 4)

print(3 != 4)

# Operadores logicos
print(3 > 4 and "hola" > 4)  # Operador &

print(3 > 4 or "hola" > "python")  # Operador ||

print(3 > 4 or ("hola" < "Python" and 4 == 5))  # Condiciones compuestas

print(not (3 > 4))  # Negacion
